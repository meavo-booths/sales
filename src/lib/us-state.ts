const NAME_TO_CODE = new Map(
  US_STATES.map((state) => [state.name.toLowerCase(), state.code]),
);

/** Accept a 2-letter code or full state name; returns canonical code or empty string. */
export function normalizeUsState(input: string): string {
  const trimmed = input.trim();
  if (!trimmed) return "";
  const upper = trimmed.toUpperCase();
  if (US_STATES.some((state) => state.code === upper)) return upper;
  return NAME_TO_CODE.get(trimmed.toLowerCase()) ?? "";
}

/** Map a US ZIP (5-digit or ZIP+4) to a 2-letter state code, or null if unknown. */
export function stateFromZip(zip: string): string | null {
  const digits = zip.replace(/\D/g, "");
  if (digits.length < 3) return null;
  const prefix = digits.slice(0, 3);
  return ZIP3_TO_STATE[prefix] ?? null;
}
